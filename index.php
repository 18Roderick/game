			<h2>Metodologías ágiles </h2></br>

			<p>En las últimas décadas, la estructura y la gestión de los proyectos dentro de las organizaciones se ha vuelto más compleja con el uso de nuevas herramientas, tecnologías, equipos de trabajo dispersos en diferentes lugares a distancia, instantaneidad en el acceso a la información, etc.
			 Ante un contexto diferente, resulta cada vez más complejo obtener resultados favorables utilizando las herramientas de gestión tradicionales, ya que no se adaptan a las nuevas expectativas de los usuarios y a las exigencias del mercado.
			La necesidad de implementar procedimientos que permitan entregar productos de calidad con los costes y tiempos pactados ha llevado al desarrollo de nuevas metodologías para la gestión de equipos y proyectos.
			El desarrollo ágil de software envuelve un enfoque de desarrollo iterativo e incremental, donde los requisitos y soluciones evolucionan con el tiempo según la necesidad del proyecto. Así el trabajo es realizado mediante la colaboración de equipos auto - organizados y multidisciplinarios, inmersos en un proceso compartido de toma de decisiones a corto plazo.
			</p></br>
			
			<h3>Antecedentes  </h3></br>

			<p>La definición moderna de desarrollo ágil de software evolucionó a mediados de la década de 1990 como parte de una reacción contra los métodos tradicionales, muy estructurados y estrictos, extraídos del modelo de desarrollo en cascada.</p></br>

			<p>Los métodos de desarrollo ágiles e iterativos pueden ser vistos como un retroceso a las prácticas observadas en los primeros años del desarrollo de software (aunque en ese tiempo no había metodologías para hacerlo). </p></br>

			<h3>Beneficios de implementar las metodologías ágiles </h3>
			<ul style="list-style-type:disc">
				<li>
				Mejoran la satisfacción del cliente debido a que lo involucra a lo largo del proyecto. En cada etapa del desarrollo se informará al cliente sobre los progresos de este. De ese modo, el cliente puede sumar su experiencia para optimizar las características del producto final. Se pueden evitar así numerosos malentendidos dado que el cliente poseerá en todo momento una completa visión del estado del producto.
				</li></br>

				<li>
				Mejora la motivación e implicación del equipo de desarrollo. Las metodologías ágiles permiten a todos los miembros del equipo conocer el estado del proyecto en cualquier momento. Los compromisos son negociados y aceptados por todos los miembros del equipo y las ideas de cualquiera de sus integrantes son tenidas en cuenta.
				</li></br>

				<li>
				Permiten ahorrar tiempo y costes. El desarrollo ágil trabaja de un modo más eficiente y rápido que otras metodologías. Además, estos procesos ponen el foco en cumplir estrictamente el presupuesto y los plazos pactados a la hora de definir y planificar el proyecto.
				</li></br>

				<li>
				Mayor velocidad y eficiencia. En las metodologías ágiles se trabaja realizando entregas parciales pero funcionales del producto. De ese modo, es posible entregar en el menor intervalo de tiempo posible una versión funcional del producto.
				</li></br>

				<li>
				Permiten mejorar la calidad del producto. La continua interacción entre los desarrolladores y los clientes tienen como objetivo asegurar que el producto final sea exactamente lo que el cliente quiere y necesita. Además, este enfoque permite abrazar la excelencia tecnológica, lo que permite obtener un producto tecnológicamente superior.
				</li></br>

				<li>
				Permiten rentabilizar las inversiones más rápidamente. Gracias a la realización de entregas tempranas el cliente tendrá rápido acceso a aquellas funcionalidades que en verdad aportan valor acelerando el retorno de la inversión.
				</li>
			
			</ul>

			<h3>Caracteristicas </h3>

			<ul style="list-style-type:disc">

				<li>Iterativo.</li></br>

				<li>Orientado a los componentes de software más que a las tareas en las que se va a alcanzar dicho objetivo.</li></br>

				<li>Tolerante a los cambios.</li></br>

				<li>Guiado por los riesgos.</li></br>

				<li>La revisión de los componentes sirve para aprender de los errores y volver a iniciar el ciclo de desarrollo.</li>
			</ul>

			<h3>Aprendizaje</h3>

			<p>La última etapa termina con una serie de ciclos de colaboración, su trabajo consiste en capturar lo que se ha aprendido, tanto positivo como negativo. Es un elemento crítico para la eficacia de los equipos. 
El aprendizaje permite mejorar el entendimiento real sobre la tecnología, los procesos utilizados y el proyecto. El aprendizaje individual permite al equipo tener mayor posibilidad de éxito.
