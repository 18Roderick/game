	<ul  style="list-style-type:disc">
	<li>Revisiones Técnicas.</li>
	<li>Retrospectivas del proyecto.</li>
	<li>Grupos de enfoque al cliente.</li>
	</ul>
<p>Las revisiones deben hacerse después de cada iteración. Ambos, los desarrolladores y los clientes examinan sus suposiciones y usan los resultados de cada ciclo de desarrollo para aprender la dirección del siguiente. El equipo aprende:</p>
	<ul  style="list-style-type:disc">
	<li>Acerca de los cambios de producto.</li>
	<li>Cambios más fundamentales en las suposiciones subyacentes sobre cómo se están desarrollando los productos.</li>
	</ul>
<p>Las iteraciones deben ser cortas, para que el equipo pueda aprender de errores pequeños en lugar de grandes.
